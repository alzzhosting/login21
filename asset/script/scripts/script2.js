function openProject1(title) {
  Swal.fire({
    title: title,
    html: `
          <p class="text-gray-300 mb-4">Detail project akan diarahkan ke halaman khusus.</p>
          <div class="flex justify-center">
            <i class="fas fa-external-link-alt text-cyan-400 text-4xl"></i>
          </div>
        `,
    icon: "info",
    background: '#1f2937',
    color: '#f3f4f6',
    confirmButtonText: "Lanjut",
    confirmButtonColor: "#00ffe0",
    showCancelButton: true,
    cancelButtonText: "Batal",
    cancelButtonColor: "#6b7280"
  }).then((result) => {
    if (result.isConfirmed) {
      window.location.href = "https://api.kenz.my.id"
    }
  });
}
